package jsftest;

public class JobDetails {

    
        // Job ID
        String ID;
        // workflow used in the job
        String workflow;
        // status of the workflow
        String status;

        
        public JobDetails(String ID, String workflow, String status) {
            this.ID = ID;
            this.workflow = workflow;
            this.status = status;
        }

        public String getID() {
            return ID;
        }

        public String getWorkflow() {
            return workflow;
        }

        public String getStatus() {
            return status;
        }

        
    

}
